public class colmena {
    
    private int id_colmena;
    private String CantidadPoblacion;
    private String CalidadPoblacion;
    private boolean PresenciaReina;
    private boolean PresenciaMiel;
    private int PanalesConCera;
    private int PanalesConAlimento;
    private int PanalesConCria;
    private int PanalesVacios;

    public int getId_colmena() {
        return id_colmena;
    }

    public void setId_colmena(int id_colmena) {
        this.id_colmena = id_colmena;
    }

    public String getCantidadPoblacion() {
        return CantidadPoblacion;
    }

    public void setCantidadPoblacion(String CantidadPoblacion) {
        this.CantidadPoblacion = CantidadPoblacion;
    }

    public String getCalidadPoblacion() {
        return CalidadPoblacion;
    }

    public void setCalidadPoblacion(String CalidadPoblacion) {
        this.CalidadPoblacion = CalidadPoblacion;
    }

    public boolean isPresenciaReina() {
        return PresenciaReina;
    }

    public void setPresenciaReina(boolean PresenciaReina) {
        this.PresenciaReina = PresenciaReina;
    }

    public boolean isPresenciaMiel() {
        return PresenciaMiel;
    }

    public void setPresenciaMiel(boolean PresenciaMiel) {
        this.PresenciaMiel = PresenciaMiel;
    }

    public int getPanalesConCera() {
        return PanalesConCera;
    }

    public void setPanalesConCera(int PanalesConCera) {
        this.PanalesConCera = PanalesConCera;
    }

    public int getPanalesConAlimento() {
        return PanalesConAlimento;
    }

    public void setPanalesConAlimento(int PanalesConAlimento) {
        this.PanalesConAlimento = PanalesConAlimento;
    }

    public int getPanalesConCria() {
        return PanalesConCria;
    }

    public void setPanalesConCria(int PanalesConCria) {
        this.PanalesConCria = PanalesConCria;
    }

    public int getPanalesVacios() {
        return PanalesVacios;
    }

    public void setPanalesVacios(int PanalesVacios) {
        this.PanalesVacios = PanalesVacios;
    }
    
    
}
